// Model answers for `02-numbers`

function addTwo(a, b) {
  return a + b;
}

//

function getSquare(a) {
  return a * a;
}

//

function getArea(a, b) {
  return a * b;
}

//

function getCircumference(radius) {
  return 2 * radius * Math.PI;
}

//

function getAverage(a, b) {
  return (a + b) / 2;
}

//

module.exports = {
  addTwo,
  getSquare,
  getArea,
  getCircumference,
  getAverage
};
